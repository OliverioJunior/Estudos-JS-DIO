// A tipagem das variáveis no JavaScript é fraca Ex:

var Nome = 'Olivério' 

console.log(typeof(Nome)) // Por está entre aspas a variável Nome é uma string, não houve a nescessidade de declarar antes que ela era uma string, por isso a tipagem do JavaScript é considerada fraca

var idade = 21;

console.log(typeof(idade)) // Agora o tipo da minha variável é number

var idade = "21";

console.log(typeof(idade)) // Com o uso das aspas o que antes era um número, virou umas string

var boolean = true 

console.log(typeof(boolean)) // Agora o tipo da variável é um boleano